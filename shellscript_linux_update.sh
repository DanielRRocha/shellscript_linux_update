clear

echo "***********************************************************"
echo "*************Updating the System!**************************"
echo "***********************************************************"
echo ""$'\n\n\n\n\n'

sudo apt update -y

sleep 1
echo ""$'\n\n\n\n\n'
echo "***********************************************************"
echo "****************Update Done!*******************************"
echo "***********************************************************"
echo ""$'\n\n\n\n\n'
sleep 3

sudo apt upgrade -y

sleep 1
echo ""$'\n\n\n\n\n'
echo "***********************************************************"
echo "****************Updated System!****************************"
echo "***********************************************************"
echo ""$'\n\n\n\n\n'

sleep 2
echo ""$'\n\n\n\n\n'
echo "***********************************************************"
echo "*************Cleaning Base!********************************"
echo "***********************************************************"
echo ""$'\n\n\n\n\n'

sleep 2
sudo apt autoremove
sudo apt autoclean
sudo apt clean

sleep 1
echo ""$'\n\n\n\n\n'
echo "***********************************************************"
echo "****************Finished!**********************************"
echo "***********************************************************"
echo ""$'\n\n\n\n\n'

exit 0
